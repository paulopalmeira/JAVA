
class variaveis {
    public static void main(String[] args) {
  
    //CASTING = moldar um número como sendo INTEIRO:        
    
    double d3 = 3.14;
    int i = (int) d3;
    System.out.println(i);

    //CASTING variáveis LONG e INT:        
    
    long x = 10000;
    int y = (int) x;
    System.out.println(y);

    }
}